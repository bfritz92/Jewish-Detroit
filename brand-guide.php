        <h2 class="">Our Message</h2>
        <h3>Consistent communications builds the understanding that the Jewish Federation and its partner agencies are a network. Stronger together.</h3>
        <p>Just as we value the Federation logo for its consistent visual message in print and digital media, our words are equally important. 
        The following are various message appropriate, “drop-in” paragraphs for use in your print, social and verbal communications. 
        Use this language to communicate what Federation is and does, as well as its relationship to your organization and the community</p>
        <h3>Use This When Communicating About Federation</h3>
        <p class="pullquote">The Jewish Federation of Metropolitan Detroit is the cornerstone of our Jewish Community. 
        We are committed to taking care of the needs of the Jewish people and building a vibrant Jewish future, in Detroit, in Israel and around the world.</p>
        <h3>Use This When Communicating About Federation and Its Agency Partners</h3>
        <div class="pullquote">
            <p>As the principle engine of Jewish philanthropy and planning in the Detroit area, the Jewish Federation of Metropolitan Detroit 
            works in partnership with its network of agencies to reach and serve the needs of people at every age and stage of life in the 
            Jewish community of Metropolitan Detroit, in Israel and around the world.</p>

            <p>Locally, Federation supports a family of 17 social service agencies, community organizations and day schools across Metro Detroit. 
            Working with its national and global partners, Federation positively impacts the lives of thousands of Jews throughout Israel, 
            in the Former Soviet Union and in 60 countries around the world.</p>

            <p>Through the Annual Campaign, Federation raises and allocates funds to its partner agencies to provide critical assistance to those in need, 
            including individuals and families, seniors, people with disabilities, and the unemployed. Federation builds and sustains a vibrant Jewish community, 
            creating avenues for Jewish education and identity, fostering close ties to Israel and engaging the next generation of Jewish Detroiters.</p>

