        <article class="brand-guide--section our-logo fade" id="our-logo">
            <h2>Our Logo</h2>
            <p>Our logo establishes our presence in all communications. It is a symbol that MUST be present in all partner agency communications. 
            Consistent use of the logo makes the JFMD brand more recognizable and strengthens Federation’s relevance to the community.</p>
            <h3>Making Our Mark</h3>
            <p>The core elements of our logo are the mark and the Jewish Federation logotype.</p>

            <p>The Federation mark is visually powerful and deeply rooted in Jewish tradition</p>
            <img class="" src="https://2019.jewishdetroit.org/wp-content/uploads/2019/08/logo-breakdown.svg" alt="Visual Breakdown of the Jewish Detroit logo">
            <ul>
                <li>The “umbrella” arcs (the rainbow) symbolize Federation as our community safety net</li>
                <li>The flame symbolizes our history and Jewish heritage. It guides the eye upward, providing a sense of aspiration and optimism.</li>
                <li>The menorah – with its clean, contemporary lines – suggests innovation and anchors our visual identity clearly in Jewish tradition.</li>
                <li>Color: The use of two variations of blue, which is viewed as a traditional Jewish color, is both optimistic and approachable.</li>
            </ul>
            <p>The mark itself may be used on its own as a graphic element to support the overall branded message as long as the whole logo is present as well. 
            NEVER use it as a replacement for the full logo in your communications</p>
            <h3>Our logo is the cornerstone of our brand and must appear in all external communications by our partner agencies.</h3>
            <p>Just as the Jewish Federation of Metropolitan Detroit and its partner agencies work together to serve the community, 
            so should our logos appear together in your key communications to the community. 
            Co-branding builds brand equity and communicates the relationship between the Federation and its partner agencies.</p>

            <h3>When to use the “Supported by” Federation Partner Logo</h3>
            <img class="logo-limit" src="https://2019.jewishdetroit.org/wp-content/uploads/2019/08/JFMD_Supported-by.svg" alt="Supported by JFMD logo">
            <p>The Federation Partnership/Agency logo is the logo that should should be used on all of your branded materials. 
            As a partner agency and recipient of Federation funding, this logo must be prominently displayed on your communication vehicles, 
            including brochures and annual reports, posters, direct mail, advertising and other promotional materials, website, email and agency stationery.</p>
            <p><strong>Use the “basic” Federation logo with your agency logo as a shared signature</strong> on marketing materials where the Federation is actively 
            partnering with your agency or sponsoring a specific program or event.</p>

            <h3>When to Use the Basic Federation Logo</h3>
            <img class="logo-limit" src="https://2019.jewishdetroit.org/wp-content/uploads/2019/08/JFMD_4C.svg" alt="Basic JFMD Logo">
            <p>The Federation mark and logotype are required and used in all Federation communications and in all communications 
            where the Jewish Federation of Metropolitan Detroit is a direct sponsor of a program or co-host of an event.</p>
            <p>Use the basic logo in all marketing materials where the Federation is actively partnering with your agency or sponsoring a specific program or event.</p>
            <p>Otherwise use the “supported by” logo to show Federation’s relationship to your agency in your general communications materials.</p>

            <h3>Centered Logo</h3>
            <img class="logo-limit" src="https://2019.jewishdetroit.org/wp-content/uploads/2019/08/JFMD_centered.svg" alt="JFMD Logo, Centered">
            <p>The centered logo, available for both “Supported By” and basic versions, was developed to provide a smaller footprint 
            without compromising clarity of the mark or readability of the logotype. It should be used in limited spaces in print, 
            e-blasts and web applications where the primary horizontal logo does not fit.</p>

            <h3>Color and Typeface</h3>
            <p>Color: the full-color logo has two distinct blue colors chosen for their traditional Jewish association.</p>
            <p>The Pantone Matching System (PMS) colors for printing on coated paper stock are lighter blue PMS 2995C and darker blue PMS 302C. 
            For printing on uncoated stock the PMS colors and 306U and 302U.</p>
            <img class="logo-limit" src="">
            <p>Use the two-color logo when prints with two or more colors and online.</p>
            <p>When used on a dark background, white is preferable.</p>
            <p>The all-black and all-white knock-out versions are permitted, but should be used only when no other option is available.</p>
            <img class="logo-limit" src="">
            <p>Our house font is Gotham, and this typeface should be used in all powerpoints and formal presentations that reflect the JFDM brand. Please contact the marketing department if you need help loading the font onto your system. </p>
            <h3>Minimum Size and “Clear” Space</h3>
            <p>Size: To retain legibility, the logo should be at least 2 inches wide or 144 pixels online.</p>
            <img class="logo-limit" src="">
            <p>“Clear space” is the width of the center flame in the logo.</p>
            <img class="logo-limit" src="">
            <p>When space is limited, the center logo should be used, but never smaller than .75 inches wide (or 225 pixels with your monitor set at a resolution of 300DPI).</p>
            <img class="logo-limit" src="">
