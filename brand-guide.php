            <h3>About Federation</h3>
            <p>The Jewish Federation of Metropolitan Detroit is dedicated to serving the needs of the Jewish people locally, globally and in Israel. 
            Federation is the community leader, and through its annual fund-raising campaign provides for a vibrant and secure Jewish community, now and in the future.</p>
            <h3>Federation and its Agency Partners</h3>
            <p>Federation is the principle engine of Jewish philanthropy and planning in metropolitan Detroit. 
            JFMD works in partnership with its network of agencies to serve the diverse needs of individuals and families in the local Jewish community, 
            in Israel, and around the world.</p>

            <p>Locally, Federation supports a family of 17 social service agencies, day schools and community organizations across metropolitan Detroit. 
            Working with its national and global partners, Federation also impacts the lives of thousands of Jews throughout Israel, 
            in the former Soviet Union and in 60 countries around the world.</p>

            <p>Through its Annual Campaign, Federation raises and allocates funds to its partner agencies to provide critical assistance to those in need, 
            including children, young adults, individuals with disabilities, seniors, the unemployed and the homeless. Federation also works to build 
            and sustain a vibrant Jewish community, creating avenues for Jewish education and identity, fostering close ties to Israel and engaging 
            the next generation of Jewish Detroiters.</p>
