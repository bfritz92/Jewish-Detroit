        <h3>Use This When Communicating About Federation’s Annual Campaign</h3>
        <p>The Annual Campaign is Federation’s single most important vehicle for securing the welfare and vitality of the Jewish community, locally, 
        globally and in Israel.Every dollar counts. Your Annual Campaign donation is allocated to Federation’s family of 17 local agencies and key national 
        and overseas partners. A strong annual campaign assures a vibrant Jewish present and future, providing services and programs to our most vulnerable 
        members and creating opportunities for Jewish identity-building, education and growth.Locally. Federation and its network of 17 agencies create 
